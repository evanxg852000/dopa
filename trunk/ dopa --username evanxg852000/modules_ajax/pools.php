	   <P>
             CIPFEM is a non-profit organization seeking to help girls and young 
			 women in Dakar, Senegal to stay in school, develop confidence, and fulfill 
			 their potentials as leaders. CIPFEM fulfills these goals by providing a nurturing
			 space where young women are tutored by local and international volunteers,
			 by providing workshops in the arts, and by facilitating sports and leadership
			 development. Through these programs, our goal is to improve test scores, 
			 confidence and leadership skills so that more young women in Senegal complete 
			 their education. By encouraging academic excellence, we hope to strengthen 
			 young women’s voices to be the leaders of tomorrow.
       </P>  
	   <P>
	   CIPFEM was inspired by studying the challenges that women face in Senegal, 
	   resulting in women’s low literacy rates and marginalization in economic, 
	   domestic, and political sectors. This has been manifested in   gender inequality
	   within the justice system, domestic division of labor, and the different 
	   cultural barriers that keep girls at home instead of in school. CIPFEM 
	   aims to encourage and educate girls between the ages of 9 to 14, which is 
	   the age group that has proven to be the most at risk of dropping out of school.
	   This organization emphasizes sports and the arts as a means for cultivating
	   self-confidence as wells as teaching leadership and independent life skills. 
	   </P>
